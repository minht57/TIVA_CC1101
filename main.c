void main(void)
{
	SysCtlClockSet(SYSCTL_SYSDIV_2_5|SYSCTL_USE_PLL|SYSCTL_OSC_MAIN|SYSCTL_XTAL_16MHZ);
	CC1101_Init();

	while(1)
	{
#ifdef TX
		UART_C1101_WriteCMD("Hello\n\r");
		SysCtlDelay(SysCtlClockGet()/30);
#else
		UART_C1101_Read(GetData,5);
		SysCtlDelay(SysCtlClockGet()/30);
#endif

	}
}

